#!/usr/bin/python3
def add(a, b):
    """This is the addition function

    Args:
        a: Ther first integer
        b: The second integer

    Returns:
        Return value. a + b
    """
    return (a + b)


def sub(a, b):
    """This is the subtraction function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        Return value. a - b
    """
    return (a - b)


def mul(a, b):
    """This is the multiplication function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        Return value. a * b
    """
    return (a * b)


def div(a, b):
    """This is the division function

    Args:
        a: The first integer
        b: The second integer

    Returns:
        Return value. a / b
    """
    return int(a / b)
